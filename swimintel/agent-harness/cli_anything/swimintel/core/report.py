def generate_report(analysis_data: dict, output_path: str) -> dict:
    """Generate a DOCX report from analysis data.

    Args:
        analysis_data: Output from analyzer.analyze_swimmer()
        output_path: Where to save the .docx file

    Returns:
        dict with status, output path, and metadata
    """
    # Write analysis data to temp JSON
    with tempfile.NamedTemporaryFile(mode="w", suffix=".json", delete=False) as f:
        json.dump(analysis_data, f, default=str)
        data_path = f.name

    # Write JS template to temp file
    with tempfile.NamedTemporaryFile(mode="w", suffix=".js", delete=False) as f:
        f.write(REPORT_TEMPLATE_JS)
        js_path = f.name

    try:
        result = subprocess.run(
            ["node", js_path, data_path, output_path],
            capture_output=True,
            text=True,
            timeout=30,
        )

        if result.returncode != 0:
            return {
                "status": "error",
                "message": result.stderr or "Unknown error",
                "stdout": result.stdout,
            }

        try:
            return json.loads(result.stdout.strip())
        except json.JSONDecodeError:
            return {"status": "ok", "output": output_path, "stdout": result.stdout}

    finally:
        os.unlink(data_path)
        os.unlink(js_path)
